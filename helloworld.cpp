#include <iostream>
using namespace std;
int a = 6;
bool b = true;
float c = 3.4245;
double d = 7.98264;
char e = 68;
string f = "abc";
int main()
{
    cout << "This is an int, which is a whole number: " << a << endl;
    cout << "This is a bool, which is a true or false: " << b << endl;
    cout << "This is a float, which is a short decimal: " << c << endl;
    cout << "This is a double, which is a long decimal: " << d << endl;
    cout << "This is a char, which is a character: " << e << endl;
    cout << "This is a string, which is multiple characters: " << f << endl;
}